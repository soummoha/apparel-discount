package com.sg.shop.model;

public class Brand {

	private int discount;

	private String name;

	public int getDiscount() {
		return discount;
	}

	public String getName() {
		return name;
	}

	public void setDiscount(int discount) {
		this.discount = discount;
	}

	public void setName(String name) {
		this.name = name;
	}

}
